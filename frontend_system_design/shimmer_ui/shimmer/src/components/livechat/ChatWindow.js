var finalName = "";

function generateRandomNames() {
  var finalName = nameList[Math.floor(Math.random() * nameList.length)];
  return finalName;
}

function ChatWindow() {
  const [messages, setMessages] = useState([]);
  const [inputMessage, setInputMessage] = useState("");
  let id = useId();
  useEffect(() => {
    const APIIntervalCall = setInterval(fetchedData, 1000);

    return () => {
      clearInterval(APIIntervalCall);
    };
  }, []);

  const fetchedData = () => {
    let data = [
      {
        name: generateRandomNames(),
        avatar: "https://picsum.photos/id/237/50/50",
        message: "this is live streaming chat",
      },
    ];

    setMessages((messages) => {
      let newMessageList = [...data, ...messages];
      newMessageList = newMessageList.splice(0, CHAT_MESSAGES_LIMIT);
      return newMessageList;
    });
  };

  return (
    <>
      <div className="flex w-full h-[350px] border border-black m-5 overflow-y-scroll flex-col-reverse">
        {messages.map((message, i) => (
          <Fragment key={id + i}>
            <ChatMessage {...message} />
          </Fragment>
        ))}
        <input
          value={inputMessage}
          onChange={(e) => setInputMessage(e.target.value)}
        />
      </div>
    </>
  );
}

export default ChatWindow;
